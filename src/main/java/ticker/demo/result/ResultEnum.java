package ticker.demo.result;

public enum ResultEnum {
    SUCCESS(1,"成功"),
    FAIL(0,"失败"),
    ;
    private Integer code;
    private String msg;

    ResultEnum(Integer code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public Integer getCode() {
        return code;
    }

    public String getMsg() {
        return msg;
    }
}